import numpy as np  # Для работы с массивами и математическими операциями
import matplotlib.pyplot as plt  # Для построения графиков

# --- ШАГ 1: Ввод данных ---
print("=== Построение калибровочного графика давления ===")

# Укажи названия файлов, в которых хранятся данные АЦП (по одному столбцу)
file_zero = 'data1.csv'  # Файл с данными при выключенном вентиляторе (P = 0 Па)
file_known = 'now.csv'  # Файл с данными при включённом вентиляторе

# Вводим давление, измеренное манометром, вручную (в Паскалях)
known_pressure_pascal = 94.5


# --- ШАГ 2: Функция загрузки данных ---
def load_column_data(filename):
    """
    Загружает данные из файла, содержащего один столбец чисел.

    Аргументы:
        filename (str): путь к файлу с данными

    Возвращает:
        numpy.ndarray: массив значений АЦП
    """
    return np.loadtxt(filename)  # Считываем все числа из файла как массив


# --- ШАГ 3: Загрузка и обработка данных ---
# Загружаем показания АЦП при нулевом давлении (вентилятор выключен)
adc_zero = load_column_data(file_zero)
# Загружаем показания АЦП при известном давлении (вентилятор включён)
adc_known = load_column_data(file_known)

# Вычисляем среднее значение АЦП для каждого случая
avg_adc_zero = np.mean(adc_zero)  # Среднее при P = 0 Па
avg_adc_known = np.mean(adc_known)  # Среднее при P = known_pressure_pascal Па

# Выводим результаты в консоль
print(f"Среднее АЦП при P = 0 Па: {avg_adc_zero:.1f}")
print(f"Среднее АЦП при P = {known_pressure_pascal} Па: {avg_adc_known:.1f}")

# --- ШАГ 4: Подготовка данных для графика ---
# Создаём два списка:
#   pressures — значения давления (в Па)
#   adc_values — соответствующие средние показания АЦП
pressures = [0, known_pressure_pascal]  # Ось X: давление
adc_values = [avg_adc_zero, avg_adc_known]  # Ось Y: средние отсчёты АЦП

# --- ШАГ 5: Построение графика ---
plt.figure(figsize=(10, 7))  # Создаём новое окно графика размером 10x7 дюймов

# Отображаем две калибровочные точки как большие красные звёзды
plt.scatter(
    pressures,  # Координаты по оси X
    adc_values,  # Координаты по оси Y
    color='#8725C2',  # Цвет точек
    s=120,  # Размер точек (в пикселях²)
    marker='*',
    label='Измерения',  # Подпись для легенды
    zorder=5  # Поверхность: точки поверх сетки
)

# --- Линейная аппроксимация ---
# Находим коэффициенты прямой ADC = k * P + b методом наименьших квадратов
k, b = np.polyfit(pressures, adc_values, 1)  # 1 — степень полинома (прямая)

# Генерируем плавный диапазон давлений для построения прямой
P_for_plot = np.linspace(0, known_pressure_pascal * 1.2, 100)  # 100 точек от 0 до 1.2*P_max
ADC_fit = k * P_for_plot + b  # Вычисляем соответствующие значения АЦП по прямой
l = 1 / k
p = b / k
# Рисуем аппроксимирующую прямую зелёным цветом, пунктиром
plt.plot(
    P_for_plot,  # Ось X: давление
    ADC_fit,  # Ось Y: расчётные значения АЦП
    color='#CF03EB',  # Цвет линии
    linewidth=2,  # Толщина линии
    #label=f'Аппроксимация: N = {k:.3f}·P + {b:.1f}'  # Подпись с формулой
    label=f'P = {l:.3f}·N - {p:.2f}'
)

# --- Настройка внешнего вида графика ---
plt.title('Калибровочный график\n зависимости показаний АЦП от давления', fontsize=14, pad=20)
plt.xlabel('Давление $P$, Па', fontsize=12)  # Название оси X
plt.ylabel('Отсчёты АЦП $N$', fontsize=12)  # Название оси Y

# Включение основной и дополнительной (минорной) сетки
plt.grid(True, which='major', alpha=0.7, linewidth=1)  # Основная сетка (толстая)
plt.grid(True, which='minor', alpha=0.4, linewidth=0.8)  # Минорная сетка (тонкая)
plt.minorticks_on()  # Включаем минорные деления

# Добавляем легенду (подпись точек и линии)
plt.legend()

# Автоматическая подгонка полей
plt.tight_layout()

# --- ШАГ 6: Отображение графика ---
plt.show()

# --- ШАГ 7: Вывод результатов ---
print("\n=== Результаты калибровки ===")
print(f"Уравнение связи АЦП и давления:")
print(f"  Отсчёты АЦП: N = {k:.5f} × P + {b:.5f}")
print(f"Обратное уравнение для пересчёта в давление:")
print(f"  Давление: P = (N - {b:.5f}) / {k:.5f}  [Па]")